// Using var
var name = "Alice";
console.log(name); // Output: Alice
name = "Bob"; // Reassigning is allowed
console.log(name); // Output: Bob

// Using let
let age = 25;
console.log(age); // Output: 25
age = 30; // Reassigning is allowed
console.log(age); // Output: 30

// Using const
const country = "India";
console.log(country); // Output: India

