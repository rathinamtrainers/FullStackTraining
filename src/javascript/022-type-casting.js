// Implicit conversion
let result1 = 5 + Number("5"); // Number is converted to string
console.log(result1); // Output: 55

// Explicit conversion
let result2 = Number("123"); // Convert string to number
console.log(result2); // Output: 123

// Boolean conversion
let isValid = Boolean(10); // 0 is falsy
console.log(isValid); // Output: false
