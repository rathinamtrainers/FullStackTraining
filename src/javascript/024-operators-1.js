let a = 5; // Binary: 0101
let b = 3; // Binary: 0011

console.log("Bitwise AND (a & b):", a & b); // Output: 1 (Binary: 0001)
console.log("Bitwise OR (a | b):", a | b); // Output: 7 (Binary: 0111)
console.log("Bitwise XOR (a ^ b):", a ^ b); // Output: 6 (Binary: 0110)
console.log("Bitwise NOT (~a):", ~a); // Output: -6 (Binary: 1010 in 2's complement)
console.log("Left Shift (a << 1):", a << 1); // Output: 10 (Binary: 1010)
console.log("Right Shift (a >> 1):", a >> 1); // Output: 2 (Binary: 0010)
