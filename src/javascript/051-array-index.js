let colors = ["Red", "Green", "Blue"];
console.log("First color:", colors[0]); // Output: Red

// Modifying elements
colors[1] = "Yellow";
console.log("Updated colors:", colors); // Output: ["Red", "Yellow", "Blue"]

// Adding a new element
colors[3] = "Purple";
console.log("Extended colors:", colors); // Output: ["Red", "Yellow", "Blue", "Purple"]
