// String
let greeting = "Hello, World!";
console.log(greeting); // Output: Hello, World!

// Number
let age = 30;
let price = 99.99;
console.log(age, price); // Output: 30 99.99

// Boolean
let isOnline = true;
let isOffline = false;
console.log(isOnline, isOffline); // Output: true false

// Null
let emptyValue = null;
console.log(emptyValue); // Output: null

// Undefined
let unassignedValue;
console.log(unassignedValue); // Output: undefined

// Symbol
let uniqueID = Symbol("id");
console.log(uniqueID); // Output: Symbol(id)
